// Variables con var, pueden tener letras, numeros, guiones bajos. No pueden iniciar con un número.


// Inicializar una variable con un valor.
var producto = 'Monitor de 24 Pulgadas';
console.log(producto);


// Las variables se pueden reasignar.
var producto = 'Monitor de 19 Pulgadas';
console.log(producto);


// JavaScript es un lenguaje de tipo dinámico, no se específica un tipo de dato.
producto = 20;
console.log(producto);


// Se pueden inicializar sin valor y asignarlo después.
var disponible;
disponible = true;

disponible = false;





// Diferencia entre Métodos y Funciones

const numero1 = 20;
const numero2 = '20';


// Esto es una función, tiene primero el nombre y en el paréntesis se pasa el valor.
console.log(parseInt(numero2));


// Esto es un método, tiene primero el nombre de la variable seguido de un punto y el nombre de un método.
console.log(numero1.toString())




// Creando un if

// Las estructuras de control van a revisar por una condición, se pueden agregar múltiples condiciones.

// De esta forma se puede revisar si un usuario esta autenticado, si un usuario tiene suficiente dinero en su tarjeta, si un usuario pagó por el curso.

const puntaje = 1000;


// Condición: puntaje es igual a 1000
if(puntaje == 1000) {
    console.log('Sí es igual');
} else {
    console.log('No es igual');
}
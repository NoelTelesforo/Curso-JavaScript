// Se manda llamar el puntaje y despues se muestra el mismo resultado inicial

puntaje++; //5


// Primero se incrementa y luego se muestra:
++puntaje; // 6


// Decrementos

let puntaje2 = 5;

// Se manda llamar el puntaje y despues se muestra el valor inicial
